-- -----------------------------------------------------------------------------
-- מטלה 8: הדגמת ROLLBACK
-- סיפור: עדכון שגוי של סטטוס המטוסים. המנהל רצה לעדכן מטוס אחד, אך בטעות עדכן את כל הצי.
-- -----------------------------------------------------------------------------

-- א. תחילת טרנזקציה
BEGIN;

-- ב. הצגת מצב בסיס הנתונים לפני העדכון (נבחר דגימה של 5 מטוסים)
SELECT aircraft_id, model, status FROM AIRCRAFT LIMIT 5;

-- ג. ביצוע העדכון (שינוי כל המטוסים לסטטוס 'Retired' בטעות)
UPDATE AIRCRAFT SET status = 'Retired';

-- ד. הצגת מצב בסיס הנתונים לאחר העדכון (ניתן לראות שכל הסטטוסים השתנו)
SELECT aircraft_id, model, status FROM AIRCRAFT LIMIT 5;

-- ה. ביצוע פקודת ROLLBACK לביטול הטעות
ROLLBACK;

-- ו. הצגת מצב בסיס הנתונים לאחר ה-ROLLBACK (הנתונים חזרו לקדמותם - 'Active')
SELECT aircraft_id, model, status FROM AIRCRAFT LIMIT 5;


-- -----------------------------------------------------------------------------
-- מטלה 9: הדגמת COMMIT
-- סיפור: עדכון רשמי של קיבולת הטרמינלים. הוחלט להגדיל את הקיבולת ב-1000 מקומות עבור טרמינלים קטנים.
-- -----------------------------------------------------------------------------

-- א. תחילת טרנזקציה
BEGIN;

-- ב. הצגת מצב בסיס הנתונים לפני העדכון
SELECT terminal_id, terminal_name, capacity FROM TERMINAL WHERE capacity < 5100 LIMIT 5;

-- ג. ביצוע עדכון קיבולת רשמי
UPDATE TERMINAL 
SET capacity = capacity + 1000 
WHERE capacity < 5100;

-- ד. הצגת מצב בסיס הנתונים לאחר העדכון (הקיבולת גדלה)
SELECT terminal_id, terminal_name, capacity FROM TERMINAL LIMIT 5;

-- ה. ביצוע פקודת COMMIT לאישור קבוע של השינויים
COMMIT;

-- ו. הצגת מצב בסיס הנתונים לאחר ה-COMMIT (השינוי נשמר לצמיתות)
SELECT terminal_id, terminal_name, capacity FROM TERMINAL LIMIT 5;