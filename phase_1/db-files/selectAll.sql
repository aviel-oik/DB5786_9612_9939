-- =============================================================================
-- selectAll.sql
-- קובץ שליפת נתונים מכל טבלאות המערכת (מערכת ניהול תעופה)
-- =============================================================================

-- 1. שליפת כל שדות התעופה
-- מציג: שם, עיר, מדינה, קוד IATA ואזור זמן
SELECT * FROM AIRPORT;

-- 2. שליפת כל המסלולים
-- מציג: מזהה מסלול, מזהה שדה מוצא ומזהה שדה יעד
SELECT * FROM ROUTE;

-- 3. שליפת כל כלי הטיס (מטוסים) בצי
-- מציג: דגם, יצרן, קיבולת נוסעים וסטטוס תפעולי
SELECT * FROM AIRCRAFT;

-- 4. שליפת כל הטיסות המתוזמנות
-- מציג: מספר טיסה, זמני המראה ונחיתה, סטטוס, והקשר למסלול ומטוס
SELECT * FROM FLIGHT;

-- 5. שליפת כל הטרמינלים
-- מציג: שם הטרמינל, מספר, קיבולת והשיוך לשדה התעופה
SELECT * FROM TERMINAL;

-- 6. שליפת כל השערים (Gates)
-- מציג: מספר שער, סטטוס (פנוי/תפוס), קיבולת והטרמינל בו הוא נמצא
SELECT * FROM GATE;

-- =============================================================================
-- שאילתות סיכום (אופציונלי - לבדיקת כמות הנתונים)
-- =============================================================================
SELECT 'AIRPORT' AS "Table", COUNT(*) AS "Total Rows" FROM AIRPORT
UNION ALL
SELECT 'ROUTE', COUNT(*) FROM ROUTE
UNION ALL
SELECT 'AIRCRAFT', COUNT(*) FROM AIRCRAFT
UNION ALL
SELECT 'FLIGHT', COUNT(*) FROM FLIGHT
UNION ALL
SELECT 'TERMINAL', COUNT(*) FROM TERMINAL
UNION ALL
SELECT 'GATE', COUNT(*) FROM GATE;